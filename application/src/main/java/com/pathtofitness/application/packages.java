package com.pathtofitness.application;

public class packages {
	
	private int Customer_id;
	private String Buyer_Name;
	private String packages;
	private String Purchasing_date;
	private String Ending_date;
	private int price;
	
	public int getCustomer_id() {
		return Customer_id;
	}
	public void setCustomer_id(int Customer_id) {
		this.Customer_id = Customer_id;
	}
	public String getBuyer_Name() {
		return Buyer_Name;
	}
	public void setBuyer_Name(String Buyer_Name) {
		this.Buyer_Name = Buyer_Name;
	}
	public String getPackages() {
		return packages;
	}
	public void setPackages(String packages) {
		this.packages = packages;
	}
	public String getPurchasing_date() {
		return Purchasing_date;
	}
	public void setPurchasing_date(String Purchasing_date) {
		this.Purchasing_date = Purchasing_date;
	}
	public String getEnding_date() {
		return Ending_date;
	}
	public void setEnding_date(String Ending_date) {
		this.Ending_date = Ending_date;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
}
